/**
 * this class contains the methods for the game
 * @author Giovanna Fiaccabrino
 */

public class WordGames {

    /**
     *
     * @param word
     * @return  parameter word (as input in main)
     */
    public String addHelloWord(String word){
        return "Hello " + word;
    }

    /**
     *
     * @param name user name
     * @param surname user surname
     * @return  returns name + surname concatenation
     */
    public String getFullName(String name, String surname){
        return name + " " + surname;
    }

}
